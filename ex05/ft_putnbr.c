#include <unistd.h>

void	ft_write_nbr(int nb)
{
	int r;

	r = nb % 10;
	if (nb / 10 != 0)
		ft_write_nbr(nb / 10);
	r = r + '0';
	write(1, &r, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
		write(1, &"-2147483648\n", 12);
	else
	{
		if (nb < 0)
		{
			write(1, &"-", 1);
			nb = -nb;
		}
		ft_write_nbr(nb);
		write(1, &"\n'", 1);
	}
}
